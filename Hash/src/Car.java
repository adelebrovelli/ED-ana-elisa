
class Car {
    String placa;
    String marca;
    String modelo;
    String cor;
    String matriculaFuncionario;

    public Car(String placa, String marca, String modelo, String cor, String matriculaFuncionario) {
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.matriculaFuncionario = matriculaFuncionario;
    }
}

